String numericSerialToAbc(int index) {
  if (index == 0) return 'a';
  if (index == 1) return 'b';
  if (index == 2) return 'c';
  if (index == 3) return 'd';
  if (index == 4) return 'e';
  if (index == 5) return 'f';
  if (index == 6) return 'g';
  if (index == 7) return 'h';
  if (index == 8) return 'i';
  if (index == 9) return 'j';
  return '-';
}
